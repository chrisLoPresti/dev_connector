const mongoose = require("mongoose");
//accessing schema from mongoose
const Schema = mongoose.Schema;

// create schema for a user
const ProfileSchema = new Schema({
  user: {
    //associate the user by their id
    type: Schema.Types.ObjectId,
    //referencing the users collection
    ref: "users"
  },
  handle: {
    //their website handle will be a required string of 40 characters max
    type: String,
    required: true,
    max: 40
  },
  email: {
    type: String
  },
  company: {
    type: String
  },
  website: {
    type: String
  },
  location: {
    type: String
  },
  status: {
    type: String,
    required: true
  },
  skills: {
    //skills will be an array of CSV strings
    type: [String],
    required: true
  },
  bio: {
    type: String
  },
  githubusername: {
    type: String
  },
  experience: [
    {
      title: {
        type: String,
        required: true
      },
      company: {
        type: String,
        required: true
      },
      location: {
        type: String
      },
      from: {
        type: Date,
        required: true
      },
      to: {
        type: Date
      },
      current: {
        type: Boolean,
        default: false
      },
      description: {
        type: String
      }
    }
  ],
  education: [
    {
      school: {
        type: String,
        required: true
      },
      degree: {
        type: String,
        required: true
      },
      fieldofstudy: {
        type: String,
        required: true
      },
      from: {
        type: Date,
        required: true
      },
      to: {
        type: Date
      },
      current: {
        type: Boolean,
        default: false
      },
      description: {
        type: String
      }
    }
  ],
  social: {
    youtube: {
      type: String
    },
    twitter: {
      type: String
    },
    facebook: {
      type: String
    },
    linkedin: {
      type: String
    },
    instagram: {
      type: String
    }
  },
  date: {
    type: Date,
    default: Date.now
  }
});

//export this model with the variable name User using the above schema
// module.exports = <Variable Name> = mongoose.model("<Collection Name>", <above schema>);
module.exports = Profile = mongoose.model("profile", ProfileSchema);
